                    <p>
                        <br />
                        ISO 27001 adalah suatu standar sistem manajemen keamanan informasi yang penerapannya
                        diorganisasi dimaksudkan untuk mengelola resiko terhadap keamanan informasi. <br><br>

                        Standar ini memberikan kerangka kerja bagi organisasi untuk mengembangkan, menerapkan, memantau
                        dan terus meningkatkan sistem manajemen keamanan informasi dan pemenuhan peraturan perundang -
                        undangan yang berlaku dan persyaratan lainnya.<br><br>

                        Informasi adalah salah satu asset penting dan sangat berharga bagi kelangsungan hidup bisnis dan
                        disajikan dalam berbagai format berupa : catatan, lisan, elektronik, pos, dan audio visual. Oleh
                        karena itu, manajemen informasi penting untuk meningkatkan kesuksusesan yang kompetitif dalam
                        semua sektor ekonomi. Tujuan manajemen informasi adalah untuk melindungi kerahasiaan, integritas
                        dan ketersediaan informasi.<br><br>

                        Sistem manajemen keamanan informasi merupakan seperangkat unsur yang saling terkait dengan
                        organisasi atau perusahaan yang digunakan untuk mengelola dan mengendalikan risiko keamanan
                        informasi dan untuk melindungi serta menjaga kerahasiaan (confidentiality), integritas
                        (integrity) dan ketersediaan (availability) informasi.<br><br>

                        Dengan disertifikasi ISO 27001, berarti organisasi anda telah mendapatkan sertifikat yang diakui
                        secara global dalam bidang sistem manajemen keamanan informasi, hal ini membuktikan bahwa
                        organisasi anda telah mengelola resiko terhadap keamanan informasi dan memiliki pengendalian
                        terkait keamanan informasi terhadap lingkungan proses bisnisnya yang mungkin menimbulkan risiko
                        atau gangguan.<br><br>

                        Manfaat Manajemen Keamanan Informasi ISO / IEC 27001 :
                    </p>
